abstract class Chair {
  final String name;
  final String structureMaterial;
  final String coating;
  final String color;
  final double price;
  final String imagePath;

  Chair({
    required this.name,
    required this.structureMaterial,
    required this.coating,
    required this.color,
    required this.price,
    required this.imagePath,
  });
}
