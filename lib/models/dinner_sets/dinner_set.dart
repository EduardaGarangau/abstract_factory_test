abstract class DinnerSet {
  final String name;
  final String structureMaterial;
  final int seats;
  final String color;
  final double price;
  final String imagePath;

  DinnerSet({
    required this.name,
    required this.structureMaterial,
    required this.seats,
    required this.color,
    required this.price,
    required this.imagePath,
  });
}
